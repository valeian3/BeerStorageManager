package com.example.beerstoragemanager.Model;

public class Beer {

    private String beerId;
    private String Name;

    public Beer(){ }

    public String getBeerId() {
        return beerId;
    }

    public void setBeerId(String beerId) {
        this.beerId = beerId;
    }

    public String getName() {
        return Name;
    }

    public void setName(String name) {
        Name = name;
    }
}
